    private void bIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bIniciarActionPerformed
        //este es como buscar en la base de datos
        Connection conexion = null;
        String usuario;
        String contrasena;
        String rol;
        try
        {
            conexion = conexi.getConnection();
            ps = conexion.prepareStatement("select *from usuario where usuario=?");
            ps.setString(1, cUsuario.getText());
            rs = ps.executeQuery();

            if (rs.next())
            {
                usuario = rs.getString("usuario");
                contrasena = rs.getString("contrasena");
                rol = rs.getString("rol");
                if ((usuario.equals(cUsuario.getText()) && contrasena.equals(cContrasena.getText())) && (rol.equals("alumno")))
                {
                    JOptionPane.showMessageDialog(null, "Bienvenido alumno");
                    VistaPrincipal=new VistaPrincipal();
                    VistaPrincipal.setNombre(usuario);
                    this.setVisible(false);
                    VistaPrincipal.setVisible(true);
                } else
                {
                    JOptionPane.showMessageDialog(null, "Error al iniciar sesion,Usuario incorrecto o contrase침a");
                    limpiarCajasLogin();
                }
            } else
            {
                JOptionPane.showMessageDialog(null, "Usuario no registrado");
                limpiarCajasLogin();
            }
            conexion.close();
        } catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_bIniciarActionPerformed

